namespace Utils.AssemblyDetails
{
	public enum CPUVersion
	{
		/// <summary>
		/// A .NET assembly that is compiled with the AnyCPU flag
		/// </summary>
		AnyCPU = 0,

		/// <summary>
		/// A file that's built for x86 machines
		/// </summary>
		x86 = 1,

		/// <summary>
		/// A file that's built for x64 machines
		/// </summary>
		x64 = 2
	}
}